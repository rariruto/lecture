def roll(turn,num)
    back(turn)
   
        @story[turn][0..num].each_with_index do |n,i|
           Window.draw_font(50,50+i*(SIZE*2),"#{n}",@font,z:5)    
        end

    if @story[turn][num]==nil
        false
    else 
        true
    end

end

#スタート
def start
    Window.draw_font(100,250,"エゴイストの物語 -デモ版-",@title,z:5)
    Window.draw_font(320,350,"press to Enterkey",@font,z:5)
end
#エンド

#main文
i=-1
page=-1
Window.loop do
    if i<0 
        start()
        if Input.key_push?(K_RETURN)
            i=0
            page=0
        end
    else
       if !roll(page,i)
            page+=1
            i=0
            if page>61
                i=-1
                break
            end
        else
            if Input.key_push?(K_RETURN)
                i+=1
            elsif Input.key_push?(K_LEFT)
                page-=1
                i=0
            end
        end
    end

end